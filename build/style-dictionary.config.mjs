// Style Dictionary v4 config (ESM).
// Reads tokens/*.tokens.json (DTCG $value/$type) and emits, into dist/:
//  - _od-tokens.css   (temporary: :root vars + theme block + scale utilities, assembled into option-d.css by build.mjs)
//  - tokens.js        (ESM, named exports: color, font, fontSize, space, radius, effect + default)
//  - tokens.d.ts      (matching type declarations)
//
// Rationale: the brand primitives (buttons, badges, hero, wordmark...) are hand-authored
// CSS (build/primitives.css) rather than generated from tokens, because they are composite
// component rules, not 1:1 token-to-variable mappings. Style Dictionary owns the token layer only.
import StyleDictionary from 'style-dictionary';
import { formats, transformGroups } from 'style-dictionary/enums';

const PREFIX = 'od';

// Groups that get a light-default + dark-override semantic alias so that
// `.od-dark` / `[data-theme="dark"]` can swap canvas/surface/text/hairline globally.
// (primary, font, fontSize, space, radius, effect stay identical across themes.)
const THEME_ALIASES = [
  { name: 'color-canvas', light: 'color-canvas-light', dark: 'color-canvas-dark' },
  { name: 'color-surface', light: 'color-canvas-light', dark: 'color-surface-card' },
  { name: 'color-surface-elevated', light: 'color-canvas-light', dark: 'color-surface-elevated' },
  { name: 'color-text', light: 'color-body-on-light', dark: 'color-on-dark' },
  { name: 'color-text-muted', light: 'color-muted-on-light', dark: 'color-muted-on-dark' },
  { name: 'color-hairline', light: 'color-hairline-light', dark: 'color-hairline-dark' },
  { name: 'color-hairline-strong', light: 'color-hairline-strong-light', dark: 'color-hairline-strong-dark' },
];

const toKebab = (segment) => segment.replace(/([a-z0-9])([A-Z])/g, '$1-$2').toLowerCase();

function cssVarName(token) {
  return `--${PREFIX}-${token.path.map(toKebab).join('-')}`;
}

// Style Dictionary v4 keeps DTCG tokens as `$value` (rather than normalizing to `.value`)
// when the source uses the $value/$type spec. We read from `token.original` to preserve the
// exact literal casing/formatting from tokens/*.tokens.json (source of truth), bypassing any
// color-space/case normalization the built-in css/js transforms would otherwise apply.
function tokenValue(token) {
  return token.original?.$value ?? token.original?.value ?? token.$value ?? token.value;
}

StyleDictionary.registerFormat({
  name: 'od/css',
  format: ({ dictionary }) => {
    const lines = dictionary.allTokens.map((token) => `  ${cssVarName(token)}: ${tokenValue(token)};`);

    const lightAliases = THEME_ALIASES.map(
      (a) => `  --${PREFIX}-${a.name}: var(--${PREFIX}-${a.light});`,
    );
    const darkAliases = THEME_ALIASES.map(
      (a) => `  --${PREFIX}-${a.name}: var(--${PREFIX}-${a.dark});`,
    );

    const fontSizeUtilities = dictionary.allTokens
      .filter((t) => t.path[0] === 'fontSize')
      .map((t) => `.od-t-${t.path[1]}{font-size:var(${cssVarName(t)});}`)
      .join('\n');

    return `/* Generated by style-dictionary from tokens/*.tokens.json. Do not edit directly. */
:root{
${lines.join('\n')}

  /* Semantic theme aliases (light default) */
${lightAliases.join('\n')}
}

[data-theme="dark"],
.od-dark{
${darkAliases.join('\n')}
  background-color: var(--${PREFIX}-color-canvas);
  color: var(--${PREFIX}-color-text);
}

.od-sans{font-family:var(--${PREFIX}-font-sans);}
.od-mono{font-family:var(--${PREFIX}-font-mono);}
${fontSizeUtilities}
`;
  },
});

StyleDictionary.registerFormat({
  name: 'od/js',
  format: ({ dictionary }) => {
    const groups = {};
    for (const token of dictionary.allTokens) {
      const [root, ...rest] = token.path;
      groups[root] = groups[root] || {};
      let cursor = groups[root];
      for (let i = 0; i < rest.length - 1; i += 1) {
        cursor[rest[i]] = cursor[rest[i]] || {};
        cursor = cursor[rest[i]];
      }
      const leafKey = rest[rest.length - 1] ?? root;
      cursor[leafKey] = tokenValue(token);
    }

    const names = Object.keys(groups);
    const exportsSrc = names
      .map((name) => `export const ${name} = ${JSON.stringify(groups[name], null, 2)};`)
      .join('\n\n');

    return `/* Generated by style-dictionary from tokens/*.tokens.json. Do not edit directly. */
${exportsSrc}

export default {
${names.map((n) => `  ${n},`).join('\n')}
};
`;
  },
});

StyleDictionary.registerFormat({
  name: 'od/ts',
  format: () => `/* Generated by style-dictionary from tokens/*.tokens.json. Do not edit directly. */
export declare const color: Record<string, string>;
export declare const font: {
  sans: string;
  mono: string;
  weight: Record<string, number>;
};
export declare const fontSize: Record<string, string>;
export declare const space: Record<string, string>;
export declare const radius: Record<string, string>;
export declare const effect: {
  halo: Record<string, string>;
};

declare const _default: {
  color: typeof color;
  font: typeof font;
  fontSize: typeof fontSize;
  space: typeof space;
  radius: typeof radius;
  effect: typeof effect;
};

export default _default;
`,
});

const sd = new StyleDictionary({
  source: ['tokens/**/*.tokens.json'],
  platforms: {
    css: {
      transformGroup: transformGroups.css,
      buildPath: 'dist/',
      files: [{ destination: '_od-tokens.css', format: 'od/css' }],
    },
    js: {
      transformGroup: transformGroups.js,
      buildPath: 'dist/',
      files: [
        { destination: 'tokens.js', format: 'od/js' },
        { destination: 'tokens.d.ts', format: 'od/ts' },
      ],
    },
  },
});

await sd.buildAllPlatforms();
